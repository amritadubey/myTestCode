package com.test;

public class SwitchDemo {

	public static void main(String[] args) {
        int x =5; 
        switch (x) {
        case 2:
        	System.out.println("case 2");
        	break;
        case 5:
        	System.out.println("case 5");
        	break;
        	default:
        		System.out.println("default");
	}

}
}